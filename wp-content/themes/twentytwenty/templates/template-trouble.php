          <h4><a id="a3"></a>３．歯が痛い、歯がしみる</h4>
          <p>ホーチミンのありが歯科では、歯がしみる場合、大きく２つの場合がと考えています。</p>
          <p>一つは虫歯、もう一つは知覚過敏です。</p>
          <h5>虫歯</h5>
          <p>明らかに歯が欠けたり、詰め物が取れたり外れているような時は虫歯治療が必要になるでしょう。たとえその歯が治してある場合でも２次カリエスといって詰め物の隙間から汚れが入り込んで虫歯を作っている場合もあります。</p>
          <p>その場合、表からは見えないこともあり、レントゲンなどの検査が必要になります。</p>
          <p>また、熱いものがしみて痛い場合などは歯の神経が炎症している場合などもありますので神経の処置が必要になります。</p>
          <h5>知覚過敏</h5>
          <p>冷たい物がしみたりする「知覚過敏」と言われるものです。歯の象牙質にある象牙細管という無数の穴から神経へ刺激が伝わるとしみるという症状がでます。歯ブラシが当たるとピリリと痛みがあったり、お水で口をゆすぐ時も痛みが出ることもあります。</p>
          <p>これはかみ合わせや歯ぎしり、歯の食いしばりの力などによってすり減ってしまい、そこに冷たい物などの刺激が加わると歯がしみます。</p>

